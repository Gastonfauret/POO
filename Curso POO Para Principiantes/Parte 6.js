//Principios de la Programacion Orientada a Objetos (POO)

//La POO se usa para que el codigo sea modular(la forma en que podemos dividir una app
//en pequeñas partes y estas parte tienen un proposito) y reutilisable.

//1 La Capacidad de modelar problemas a travez de objetos.
    //- Asociacion.
    //- Agregacion.
    //- Composicion.

//2 Soporte de algunos principios que garantizen la modularidad y la reutilizacion de codigo.
    //- Encapsulamiento.
    //- Herencia.
    //- Polimorfismo.

//Definiciones.

//Asociacion: La asociación se podría definir como el momento en que dos objetos se unen para trabajar juntos y así, 
//alcanzar una meta. Un punto a tomar muy en cuenta es que ambos objetos son independientes entre sí, 
//veremos un poco más adelante qué implicación tiene esto.

//Agregacion: puede definirse como la situación en la que los objetos de una clase pueden acceder a las entidades de otra clase.

//Composicion: Es el mecanismo en el cual una clase se construye a partir de otros objetos de igual o distinto tipo, 
//pudiéndolos combinar para obtener la funcionalidad deseada. 
//En la composición la nueva clase, mantiene una relación “Usa/Tiene un” (Uses/Has a), 
//con los objetos que son parte de la clase.

//Encapsulamiento: es un mecanismo para reunir datos y métodos dentro de una estructura ocultando 
//la implementación del objeto, es decir, impidiendo el acceso a los datos por cualquier medio que no 
//sean los servicios propuestos.

//Herencia: Es el mecanismo por el cual una clase permite heredar las características (atributos y métodos) de otra clase. 
//La herencia permite que se puedan definir nuevas clases basadas de unas ya existentes a fin de reutilizar el código, 
//generando así una jerarquía de clases dentro de una aplicación.

//Polimorfismo: es la capacidad que tienen ciertos lenguajes para hacer que, 
//al enviar el mismo mensaje (o, en otras palabras, invocar al mismo método) desde distintos objetos, 
//cada uno de esos objetos pueda responder a ese mensaje (o a esa invocación) de forma distinta.

//Abstraccion: La abstracción consiste en aislar un elemento de su contexto o del resto de los elementos que lo acompañan. 
//En programación, el término se refiere al énfasis en el "¿qué hace?" más que en el "¿cómo lo hace?".